import re


def split_text(text, split_char='\n', line_up= False):
    """
    将文本按行分割为列表
    """
    lines = str(text).split(split_char)
    print("字段切分后lines：", lines)
    if line_up:
        line = lines[0]
        print("字段切分后line取上部分：", line)
    else:
        line = lines[1:]
        print("字段切分后line取下部分：", line)
    return str(line)

def extract_stock_codes(text):
    """
    从文本中提取股票代码
    """
    # 定义匹配股票代码的正则表达式模式
    pattern = r'\((\d{6}\.[A-Z]{2,3})\)'
    pattern_number =  r'\s*(\d{6})\s*'  # 匹配 6位数字 格式

    # 查找所有匹配的股票代码
    matches = re.findall(pattern, text)
    if matches == []:
        print("取出股票6位代码不带后缀")
        matches = re.findall(pattern_number, text)
    return matches


def extract_stock_codes_with_names(text):
    """
    提取股票代码及其对应的公司名称
    """
    # 匹配股票名称和代码的正则表达式
    pattern = r'\*\*([^(]+)\s*\((\d{6}\.[A-Z]{2,3})\)'

    matches = re.findall(pattern, text)

    stock_dict = {}
    for name, code in matches:
        # 清理股票名称，去除多余的空格
        clean_name = name.strip().replace('*', '')
        stock_dict[clean_name] = code

    return stock_dict


